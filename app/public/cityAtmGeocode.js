var geoCoding = {
    max: 0,
    db: [],
    dbref:"",
    init: function(addressDB, dbRef, startNo){
        this.max = addressDB.length;
        this.db = addressDB;
        this.dbRef = dbRef
        this.code(startNo);
    },
    code: function(no){
        let geocoder = new google.maps.Geocoder();
        let that = this;

        geocoder.geocode( { 'address': that.db[no].address}, function(results, status) {
            if (status == 'OK') {

                let objec = {
                    coor:{
                        lat:results[0].geometry.location.lat(),
                        lng:results[0].geometry.location.lng()
                    },
                    type:that.db[no].type,
                    name:that.db[no].name,
                    address:that.db[no].address,
                    openHour:that.db[no].openHour
                }
                console.log(objec)
                firebase.database().ref(that.dbRef+no).set(objec);

                no++;

            if(no<that.max){
                setTimeout(function () {
                    that.code(no)
                }, 1250);
            }


            } else {
                if(status === 'ZERO_RESULTS'){
                    no++
                    setTimeout(function () {
                        that.code(no)
                    }, 1250);
                }else{
                    location.reload();
                    console.log(that.db[no].address);
                }
                console.log(status)
            }
        });
    }
}

